package pl.paszczak;

public class Calc {
    private int x;
    private int y;

    public Calc(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Calc() {
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }


}
